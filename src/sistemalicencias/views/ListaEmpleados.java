    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        EmpleadoData ed = new EmpleadoData();
        try{
            int filaSeleccionada = jtTable.getSelectedRow();
            int estado = (int) tabla.getValueAt(filaSeleccionada, 4);
            String dni = (String) tabla.getValueAt(filaSeleccionada, 2);
            
            if(jtTable.getSelectedRow() > -1){
                
            if(estado == 1){
                int x = JOptionPane.showConfirmDialog(this, "¬øDar de baja al empleado seleccionado?",
                "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
                    if (x == 0) {
                        ed.cambiarEstado(0, dni);
                        jtDocumento.setText("");
                    }
                
            } else if(estado == 0){
                int x = JOptionPane.showConfirmDialog(this, "¬øQuitar baja al empleado seleccionado?",
                "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE);
                    if (x == 0) {
                        ed.cambiarEstado(1, dni);
                        jtDocumento.setText("");
                    }
                
            }
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un empleado", "Error", JOptionPane.WARNING_MESSAGE);
            }
       } catch(java.lang.ArrayIndexOutOfBoundsException error){
           JOptionPane.showMessageDialog(this, "Seleccione un empleado", "Error", JOptionPane.WARNING_MESSAGE);
       }
       
       listarEmpleados();
       
    }//GEN-LAST:event_jButton1ActionPerformed

