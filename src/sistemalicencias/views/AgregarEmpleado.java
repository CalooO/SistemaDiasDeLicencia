    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        EmpleadoData ed = new EmpleadoData();
        
        try {
            
                if (!jtApellido.getText().isEmpty() && !jtNombre.getText().isEmpty() && jtFecha.getDate() != null && !jtDocumento.getText().isEmpty()) {
                    String apellido = jtApellido.getText();
                    String nombre = jtNombre.getText();
                    String documento = jtDocumento.getText();
                    String telefono = jtTelefono.getText();
                    Date fecha = jtFecha.getDate();
                    
                    Empleado empleado = new Empleado();
                    empleado.setApellido(apellido);
                    empleado.setNombre(nombre);
                    if(jtTelefono.getText() == null){
                        empleado.setTelefono("No ingresado");
                    } else {
                        empleado.setTelefono(telefono);
                    }
                    empleado.setDni(documento);
                    empleado.setFechaIngreso(fecha);
                    
                    ed.guardarEmpleado(empleado);
                    
                    jtNombre.setText("");
                    jtApellido.setText("");
                    jtDocumento.setText("");
                    jtTelefono.setText("");
                    jtFecha.setDate(null);
                    
                }  else {
                    JOptionPane.showMessageDialog(this, "Quedan campos vacios", "Error", JOptionPane.WARNING_MESSAGE);
                }

             

        } catch (java.lang.RuntimeException e) {
            JOptionPane.showMessageDialog(this, "??", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

